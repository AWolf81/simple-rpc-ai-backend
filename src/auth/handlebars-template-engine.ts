  /**
   * Render a template with data
   */
  async render(templateName: string, data: HandlebarsTemplateData): Promise<string> {
    const viewsDir = path.join(__dirname, '../views');
    const templatePath = path.join(viewsDir, `${templateName}.hbs`);
    
    // Prepare template data with configuration
    const templateData = {
      // Template data
      ...data,
      
      // Branding configuration
      appName: this.config.branding?.appName,
      appLogo: this.config.branding?.appLogo,
      favicon: this.config.branding?.favicon,
      primaryColor: this.config.branding?.primaryColor,
      secondaryColor: this.config.branding?.secondaryColor,
      backgroundColor: this.config.branding?.backgroundColor,
      textColor: this.config.branding?.textColor,
      
      // Styling
      customCSS: this.config.customCSS || '',
      darkMode: this.config.darkMode,
      
      // Page metadata
      title: 'Sign In',
      
      // Custom variables
      ...this.config.variables
    };
    
    return this.hbs.renderView(templatePath, templateData);
  }
  
  /**
   * Update configuration
   */
  updateConfig(config: HandlebarsTemplateConfig) {
    this.config = { ...this.config, ...config };
  }
  
  /**
   * Get current configuration
   */
  getConfig(): HandlebarsTemplateConfig {
    return this.config;
  }
