/**
 * Handlebars-based Template Engine for OAuth Pages
 * 
 * Cleaner, more maintainable templating using express-handlebars
 * with customizable branding and styling for OAuth authentication pages.
 */

import { create } from 'express-handlebars';
import path from 'path';
import { fileURLToPath } from 'url';

// Get directory path for ES modules
const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

export interface HandlebarsTemplateConfig {
  /** Application branding */
  branding?: {
    appName?: string;
    appLogo?: string;
    favicon?: string;
    primaryColor?: string;
    secondaryColor?: string;
    backgroundColor?: string;
    textColor?: string;
  };
  
  /** Custom CSS to override default styles */
  customCSS?: string;
  
  /** Template variables */
  variables?: Record<string, any>;
  
  /** Enable dark mode support */
  darkMode?: boolean;
}

export interface HandlebarsTemplateData {
  /** Available identity providers */
  providers: Array<{
    name: string;
    displayName: string;
    loginUrl: string;
    icon?: string;
  }>;
  
  /** OAuth flow context */
  context?: {
    redirectUri?: string;
    clientId?: string;
    scopes?: string[];
  };
  
  /** Error information */
  error?: {
    code: string;
    message: string;
  };
}

